/* Function for Cakes */
function setVisibility(id) {
if(document.getElementById('bt1').value=='Hide Cakes'){
document.getElementById('bt1').value = 'Cakes';
document.getElementById(id).style.display = 'none';
}else{
document.getElementById('bt1').value = 'Hide Cakes';
document.getElementById(id).style.display = 'inline';
}
}

